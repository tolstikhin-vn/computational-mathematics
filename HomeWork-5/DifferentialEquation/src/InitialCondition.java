public class InitialCondition {
    private double xValue;
    private double fxValue;

    public InitialCondition(double xValue, double fxValue) {
        this.xValue = xValue;
        this.fxValue = fxValue;
    }

    public double getXValue() {
        return xValue;
    }

    public double getFxValue() {
        return fxValue;
    }
}
